class bitWiseOp{
    public static void main(String[] args) {
        int a  = 2;
        int b =3;
        System.out.println(a&b);
        System.out.println(a|b);
        System.out.println(a<<2);
        System.out.println(~a);
        System.out.println(a^b);
        System.out.println(b>>1);
        

    }
}